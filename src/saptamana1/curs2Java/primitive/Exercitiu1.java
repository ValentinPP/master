package saptamana1.curs2Java.primitive;

/**
 * Primitive:
 * ****************
 * byte,
 * short,
 * int, -> folosit in practica
 * long,
 * float,
 * double, -> folosit in practica
 * boolean, -> folosit in practica
 * char
 * ******************
 * */
public class Exercitiu1 {
	public static void main (String [] args){
		int x = 10;
		char val = 'a';
		char y = 83;
		double b = 2.3;
		boolean c = true;
		float f = 2.3f;

		String animal = "pisica";

		System.out.println("Valoarea lui x este: " + x);
		System.out.println("Valoarea lui val este: " + val);
		System.out.println("Valoarea lui y este: " + y);
		System.out.println("Valoarea lui b este: " + b);
		System.out.println("Valoarea lui c este: " + c);
		System.out.println("Valoarea lui f este: " + f);
		System.out.println("Valoarea lui animal este: " + animal);
	}
}
