package saptamana1.tema.problema3.primitive;

/**
 * Rezolvati greselile din acest cod
 */
public class Primitive2
{
	public static void main(String[] args)
	{
		double x = 3.2;
		double y = 5.6;
		double z = 2.3;

		double mediaAritmetica = (x+y+z) / 3.0;
		System.out.println("Media aritmetica pentru Double este: " + mediaAritmetica);

		double a = (int)3.2;
		double b = (int)5.6;
		double c = (int)2.3;

		double mediaAritmetica2 = (a+b+c) / (int)3.0;
		System.out.println("Media aritmetica pentru Integer este: " + mediaAritmetica2);

	}
}
